<h3>Trois Rivières, 4 Oct 06</h3>
<div class="story noSpaceBelow">
   <p>The <a href="http://www.pc.gc.ca/lhn-nhs/qc/saintmaurice/index_e.aspTrois Rivières" target="_blank" class="external">Forges of Saint-Maurice</a> near Trois Rivières (Three Rivers) was a bit of a disappointment. It has a &quot;gem&quot; rating in our guidebook as Canada&rsquo;s first ironworks but there is very little there that is original. The exhibits showing what they made and how they did it were educational though. Given the primitive understanding of geology at the time, I was interested to see that prospectors went looking for swamps in search of <a href="http://en.wikipedia.org/wiki/Bog_iron" target="_blank" class="external">bog iron</a> rather than the iron ore mined today. Fortunately it was midway between Montreal and Quebec so it wasn&rsquo;t much of a diversion.</p>
