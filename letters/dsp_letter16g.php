<h2>Guns, germs and steel</h2>
<div class="story">
	<p>I was enormously impressed by Roman achievements two thousand years ago and what stone masons were doing in the 1200s. The Chinese were doing similar things at similar times. I read a book on European exploration and conquest up to 1600 and was astonished to learn that a few hundred Spanish conquistadors defeated the Aztec and Inca empires, each with millions of subjects. There was no Iron Age in Australia or the Americas and in the Americas, only the Maya developed writing. Why didn't the aborigines discover Captain Cook instead of the other way around? The question is important as we might think that Eurasians are smarter than Africans, native Americans and Australian aborigines but that is essentially racist.</p>
	<p class="endText">Jared Diamond has written a Pulitzer-prize winning book attempting to answer this question and I urge you to read it: <i>Guns, Germs and Steel: Fates of Human Societies</i>. He argues it is largely due to the plant and animal species available for domestication and the shape of the continents. Indeed, they are smarter than us. See a <a href="http://www.edge.org/3rd_culture/diamond/diamond_p1.html" target="_blank">summary</a>.</p>
</div>

