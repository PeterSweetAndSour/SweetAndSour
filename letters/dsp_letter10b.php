<h2>Australia</h2>
<div class="story">
   <p>On returning to Sydney, there were several things I noticed:</p>
   <ul>
   	<li>everyone here speaks with an Australian accent&mdash;I'm sure they didn't when I left three years ago</li>
   	<li>it is difficult to drive from one side of town to the other&mdash;Sydney roads are not laid out on a neat grid (or a neat anything) and the population is roughly double.  I've also decided I prefer 4-way "Stop" signs to round-abouts.</li>
   	<li>there are pedestrians unlike Denver where most people only move inside a car.  I'm sure this is why we do not follow the American system where you can turn right (equivalent to left here) at a red traffic light since motorists checking for oncoming traffic would run over pedestrians crossing against the lights.</li>
   	<li>my hair is curlier&mdash;I'm not sure if it is the higher humidity or the water which has much lower dissolved mineral levels.  Either way, I don't like it.</li>
   	<li>there are birds&mdash;apart from Canadian geese, there are very few birds in Denver, presumably because they can't survive through the winter.</li>
   </ul>
</div>

