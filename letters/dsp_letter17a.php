<h2>Work</h2>
<div class="story">
   <p>After being unemployed for 7 months in 2003, I finally landed a job with <a class="external" href="http://www.wallstondemand.com" target="_blank">Wall St On Demand</a> which does web sites for finance companies. Towards the end, I was practicing &quot;<i>Do you want fries with that?</i>&quot; as I feared it was going to be a requirement at my next job. Anyway, I&rsquo;ve learned many things that I didn&rsquo;t even know that I needed to know.  It is the first job I&rsquo;ve had where there are a lot of people (over 20) doing similar things to me and it is great to see what everyone else is doing and hear ideas bounced around the office. Another good thing is that I don&rsquo;t have to worry about what the site looks like; other people with graphic design backgrounds work that out and my job is to make it function. At 40, I think I&rsquo;m the oldest in the Design &amp; Development group. One big drawback of the job is that it 60km/37 miles from home. It is against my environmental principles to drive that far every day but being without work is against my principles too, only more so.</p>
   <p>Lan was at American Express but they had incredibly lax data security and management resisted doing anything about it so Lan was constantly undoing what others had altered. Indeed we wondered if there was fraud with senior managers getting kickbacks but we had no proof. Lan&rsquo;s complaints put her offside with her boss and so she was sent away to the Phoenix, Arizona office. For 3 months we got up at 3:45am on Monday so we could get her to the 6am flight and she returned at 10pm on Friday.</p>
   <p>I can&rsquo;t imagine any reason for the city&rsquo;s existence except that it might have been founded by missionaries to teach unbelievers what the fires of Hell will be like. I visited Lan in May (spring) and it was already unreasonably hot. </p>
   <p class="endText">Lan was there through to the end of June 2004. Her life was made more miserable by being without a car and real cooking facilities in her hotel room, so she was limited to restaurants that she could walk to from her hotel in the late afternoon heat. However, she made friends among the drivers of the hotel shuttle bus, almost all Mexican, who took her to and from work. I think they appreciated Lan&rsquo;s kind words and shared immigrant experience.</p>
</div>
