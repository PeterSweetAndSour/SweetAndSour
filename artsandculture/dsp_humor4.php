<h4>MARTIN MOLLOY</h4>
<div class="story">
   <p>Often crude, always silly and sometimes hilarious, this comedy duo hosts Australian afternoon drivetime radio.  <i>"Please explain"</i> was their best effort, with my favourite caller being the man enquiring about high voltage power lines.  He was wondering why the sign on the fence at the base of the tower said <i>"DANGER!  KEEP OUT! 500,000 volts.  DEATH BY ELECTROCUTION!"</i> and then in smaller letters <i>"Penalty for trespassing $500"</i>.</p>
   <p>Lan and I often repeat Mick's suggestion on a phrase that could be used in a commercial for dairy-based spreads, <i>"Well bugger me.  It <b>IS</b> butter!"</i>.</p>
   <p class="endText">Their web site was www.martinmolloy.com.au but it seems to have gone.</p>
</div>

