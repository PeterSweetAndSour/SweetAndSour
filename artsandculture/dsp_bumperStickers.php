<h3>Purpose statement</h3>
<div class="story">
	<p class="endText">To entertain the class with a largely frivolous examination of bumper stickers.</p>
</div>

<h3>Thesis</h3>
<div class="story">
	<p class="endText">Bumper stickers are very American and they indicate a lot about the driver.</p>
</div>

<h3>Introduction</h3>
<div class="story">
   <p>Let me start by posing a question:<br>
   <span class="sticker">Did you ever stop to think &ndash; and then forget to start again?</span>
	<p>Some of you will recognize this as being from a bumper sticker and bumper stickers are my topic today. When I came to America, I was struck by the tremendous variety of bumper stickers and I believe that in their own special way, they make up one of your cultural treasures.</p>
	<p>Like speeches, they can be to inform, persuade or entertain.</p>

   <p>Today I want to:</p>
   <ul class="endText">
   	<li>look briefly at what bumper stickers say about America and Americans in general, and</li>
   	<li>look in detail at what they say about the individual drivers.</li>
   </ul>
</div>

<h3>What bumper stickers say about America?</h3>
<div class="story">
   <ul class="endText">
   	<li>America is <span style="color:red">motorized</span>. I am guessing that all of you came to college today in a private motor vehicle.Â  Most of you probably own your own car. Obviously without cars, there are no bumper stickers - where do you put a bumper sticker if you chief mode of transport is a bicycle, or a bus?</li>
   	<li>American's are <span style="color:red">self-expressive</span>. That there are so many more bumper stickers here than what I've seen in Australia, or Europe. It tells me that Americans are more likely to try and stand out as individuals, rather than blend in to a group.</li>
   	<li>America is a <span style="color:red">free country</span>. You may have seen the sticker that says <span class="sticker">I love my country; I fear my government</span>. The mere fact that you can leave this sticker on the car and not be pulled over by the secret police indicates what a wonderfully free country this is and that these people have little to worry about. I doubt you would last 10 minutes if you did the same in Saddam Hussein's Iraq.</li>
   </ul>
</div>

<h3>What bumper stickers say about the driver</h3>
<div class="story">
   <p>(I had only 4 minutes for this section, so I had to cut from "children" onwards.)</p>
   <p class="endText">Turning now to what bumper stickers say about the drivers.</p>
</div>

<h4>POLITICS</h4>
<div class="story">
   <p>Since the election is just a few weeks away, perhaps we should start with politics.  It is obvious that someone who puts a sticker on their car that says <span class="sticker">George W. Bush for President</span> or <span class="sticker">Gore 2000</span> wants you to vote with them for their preferred candidate. However, I'm more interested in the witty stickers that seek to advance one candidate by trivializing the other.</p>
   <p>The only interesting one I've seen so far in this campaign is <span class="sticker">Al Gore is a risky scheme</span> picking up on comments he made in the campaign.</p>
   <p class="endText">Or consider this from 1996, <span class="sticker">Dole for Pineapple</span></p>
</div>

<h4>WISDOM</h4>
<div class="story">
   <p>Next we might consider folks who care about their world and hope that we might too. Words of wisdom. These are rarely found on expensive, late-model vehicles, their owners being more concerned with other things.</p>
   <ul class="endText">
   	<li><span class="sticker">If you want peace, work for justice.</span></li>
   	<li><span class="sticker">Conflict is inevitable, violence is not.</span></li>
   	<li><span class="sticker">If you can't change your mind, are you sure you still have one?</span></li>
   	<li>and for those who decided not to take this class:<br><span class="sticker">If you think education is expensive, try ignorance.</span></li>
   </ul>
</div>

<h4>CONCERN FOR ENVIRONMENT</h4>
<div class="story">
   <p>There are many who are concerned about the state of our environment and the creatures that live in it. </p>
   <p>You have no doubt seen those stickers that say:<br><span class="sticker">ASK FIRST before hunting on private land.</span><br>encouraging us to respect property rights. Of course, hunting is more than just property rights, so there is a similar sticker with a different theme:<br><span class="sticker">ASK FIRST if the animal wants to be killed.</span> </p>
   <p class="endText">Taking the global view, <span class="sticker">Man is not the only species on the planet &ndash; we just act like it.</span> <br>On the other hand, there are those that take the opposing view: <br><span class="sticker">EARTH FIRST. We'll mine the other planets later.</span> </p>
</div>

<h4>MEN</h4>
<div class="story">
   <p>You have probably noticed that this class is overwhelming female. Ladies, I'll let you make your own judgements on the type of person who would put these on their vehicles:</p>
   <ul class="endText">
   	<li><span class="sticker">The more I drink, the better you look.</span></li>
   	<li><span class="sticker">The more I learn about women, the more I love my truck.</span></li>
   	<li><span class="sticker">My wife says that if I go fishing one more time, she's going to leave me. Gee I'm going to miss her.</span></li>
   	<li><span class="sticker">On the eighth day, God created beer.</span></li>
   </ul>
</div>

<h4>WOMEN</h4>
<div class="story">
   <p>Of course, women own cars as well, and also have opinions to express:</p>
   <ul class="endText">
   	<li>Picking up on the hit song that became an anthem of liberation, <span class="sticker">I am stong.  I am invincible.  I am TIRED.</span></li>
   	<li><span class="sticker">Human milk for human babies</span> to promote breast feeding.</li>
   	<li>Or how about this: <span class="sticker">I still miss my ex. &dash; but my aim is improving.</span></li>
   	<li>Perhaps some are better off not to express an opinion: <span class="sticker">Blondes are NOT dumb!</span> (upside down).</li>
   </ul>
</div>

<h4>PEOPLE WITH WEIGHT ISSUES</h4>
<div class="story">
   <p>Unfortunately, I didn't get a chance to see the driver of either of these vehicles to assess their physique:</p>
   <ul class="endText"></li>
   	<li><span class="sticker">I may be FAT, but you're UGLY and I can lose weight.</span></li>
   	<li>and this one stating the obvious: <span class="sticker">Fat people are harder to kidnap.</span></li>
   </ul>
</div>

<h4>CHILDREN</h4>
<div class="story">
   <p>Having covered adults, lets now look at children and the joy they can bring. The owners of these cars are concerned that not every child is properly cared for:</p>
   <ul class="endText">
   	<li><span class="sticker">A world of wanted children would make a world of difference.</span></li>
   	<li>and a public safety message: <span class="sticker">Hug your kids at home. Belt them in the car.</span></li>
   	<li>and there are those who just enjoy being with them: <span class="sticker">If we had known that grandchildren were so much fun, we would have had them first!</span></li>
   </ul>
</div>

<h4>PROUD OF HOME</h4>
<div class="story">
   <p>It is natural to be proud of where you live. Why not express that on your car. Consider these:</p>
   <ul class="endText">
   	<li><span class="sticker">COLORADO: So much snow.  So little time.</span></li>
   	<li><span class="sticker">Welcome to Colorado. Now go home.</span></li>
   	<li><span class="sticker">Leaving Florida? Take a developer with you.</span></li>
   	<li><span class="sticker">On earth as it is in Texas</span></li>
   </ul>
</div>

<h4>RELIGION</h4>
<div class="story">
   <p>Some people have deep religious convictions and seek to share those with others. Some people don't want them shared:</p>
   <ul class="endText">
   	<li><span class="sticker">God is too big for one religion.</span></li>
   	<li>Others take a practical view: <span class="sticker">Don't drive faster than your angel can fly.</span> and</li>
   	<li><span class="sticker">Jesus is coming. Look busy.</span></li>
   </ul>
</div>

<h4>JUST PLAIN SILLY</h4>
<div class="story">
   <p>Finally there are those that just seek to have a bit of fun as these examples illustrate:</p>
   <ul class="endText">
   	<li><span class="sticker">Of all the things I've lost, I miss my mind the most.</span></li>
   	<li>We have all seen the one that says: <br><span class="sticker">DARE to keep kids off drugs</span><br>but have you also seen the one that says: <br><span class="sticker">DARE to keep cops off donuts</span>?</li>
   </ul>
</div>

<h3>Conclusion</h3>
<div class="story">
   <p>We have seen then that bumper stickers tell us something about this great country and a great deal about the drivers that live in it</p>
   <p>Perhaps you are wondering what bumper sticker I have on my car. Actually, I haven't found the bumper sticker that really sums me up in ten words or less. Maybe I'll have to have it made. Here is what it would say: <span class="sticker">My plan is to live forever. So far I'm on schedule!</span></p>
   <p class="endText">Having stopped to think about bumper stickers, you can now start again.</p>
</div>
