<h2>Conclusion</h2>
<div class="story">
	<p>What then of the future?</p>
	<p>While Jewish groups have had some success in retrieving art works stolen by the Nazis and bank deposits taken by the Swiss, the Isrealis seem incapable of seeing that the Palestinians might want their homes and land back.</p>
	<p>There will be no peace in the Middle East until the issue of the Palestinians has been addressed, both for those inside what is now Isreal and the Occupied Territories and those in other countries.  People don't give themselves to be suicide bombers unless their situation is utterly hopeless and that is the state Palestinians find themselves in now.,/p>
	<p>The &quot;concessions&quot; promoted by the Israelis in negotiations mean almost nothing to Palestinians given that the starting point for negotiations is the Isrealis having control of everything, not the situation as it existed in 1918 at the end of WW1,  or before 1948, or even before 1967-each time they lost more control over their destiny.</p>
	<p>The obvious solution is a Palestinian homeland, completely autonomous from Israel.  So far, that has not been offered.   Refusal to be fair with the PLO and then the Palestinian Authority has given popular support to the more radical groups such as Hamas which is still committed to the destruction of Israel.</p>
	<p>Was it wrong for the Resistance in Nazi-occupied Europe to kill Nazi soldiers and bomb Nazi targets?  Of course not.  Remember that aparthied South Africa imprisoned Nelson Mandela for being a terrorist.</p>
	<p>Whether someone is a &quot;terrorist&quot; or a &quot;freedom fighter&quot; depends on which side you think justice is on.  We must not accept labels given in the media without questioning.</p>
	<p>Finally, America's blind support for Israel has naturally made it a target for attack as we saw last week with the attack on the USS Cole in Yemen.  You should also be aware that, as reported on NPR, the land America proposes to build its new Embassy on in Jerusalem was confiscated from its Palestinian owners after 1948.</p>
	<p class="endText">I hope then that if you see the news tonight, you will understand why there is so much anger in the Middle East.  While I can't condone violence, please understand that the Palestinians have been left with absolutely no alternative.</p>
</div>

