<h2>Thesis</h2>
<div class="story">
	<p class="endText">While the Middle East is often in the news, the causes of the conflict are now so old that it is no longer &quot;news&quot; so this speech seeks to give an overview of the Palestinian situation and try to explain the violence we see.</p>
</div>

<h2>Introduction</h2>
<div class="story">
	<p>I am married to a refugee.  My wife left Vietnam on tiny, crowded boat in 1981 and spent 3 months in a refugee camp in Singapore before the Australian government offered to resettle her and allowed her to become an Australian citizen.  As a refugee, she has endured uncertainty and hardships but they were relatively short-lived.</p>
	<p>Today, there is again bloodshed in the Middle East.  Dr Weizmann, one of the founding fathers of Zionism and Israel's first President wanted Jews to settle in Palestine without violating the legitimate interests of the Arabs, writing <i>'not a hair of their heads shall be touched"</i>.</p>
	<p>Now we see armoured vehicles and helicopter gunships taking on mainly unarmed groups of Palestinians.  The Economist reports that over 100 people have been killed on the last 2 weeks of violence and all but 7 of them are Palestinian.  What happened?  Where did Dr Weizmann's vision of mutual co-operation go wrong?</p>
	<p>By it's nature, &quot;news&quot; is what is "new" and yet, we must look back over the last century to understand what is happening now.  Many Palestinians have been refugees for up to 52 years!  I want to demonstrate that the Palestinians have legitimate grievances and have been allowed no way to address them and this has led to violence.</p>
	<p>We will look at three periods:</p>
	<ol>
		<li>before the establishment of Isreal in 1948
		<li>from 1948 to the &quot;6 Day War&quot; in 1967
		<li>since 1967
	</ol>
</div>

<h2>Body</h2>

<h3>Before 1948</h3>
<div class="story">
	<ul class="endText">
		<li>After suffering centuries of persecution in Europe and particularly Czarist Russia, Austrian Jew Theodore Herzl published &quot;The Jewish State&quot; in 1896 and formally started the Zionist movement to create a Jewish homeland in Palestine.  A few Jews did start emigrating but the numbers were small.  Combined with the native Jewish population,  Brittanica online says that in 1914, only 12% of the population was Jewish.</li>
		<li>At that time and for almost the previous 400 years, Palestine was ruled Ottoman Turks</li>
		<li>During WW1, Britain made promises to the Zionists culminating in the <a href="http://en.wikipedia.org/wiki/Balfour_Declaration_of_1917" target="_blank" class="external">Balfour Declaration</a> of 1917 which declared support for a Jewish homeland.</li>
		<li>Unfortunately, they also made promises to Arab leaders about gaining independence after the war.</li>
		<li>With the collapse of the Ottoman empire at the end of WW1, the middle east was divided between the European powers and Palestine was under a British Mandate</li>
		<li>It was clear to both Arabs and Jews that the future of the area when the Mandate ended would be determined by the relative size of populations and ownership of the land.  Arabs therefore sought to halt Jewish immigration, Jewish groups tried to increase it.</li>
		<li>Nazi persecution in the 1930s boosted immigration so the Jewish portion of the population reached 30% before WW2 bringing widespread resentment among native Arabs causing riots at various times</li>
		<li>With the end of WW2 and the Holocaust in Europe, the British position in Palestine became untenable.  Jewish refugees from all over Europe were now desperate to enter Palestine and the British tried to turn most of them away leading to Jewish resentment and violence.  Similarly, the Arab population was alarmed at the prospect of being overwhelmed and demanded the British let them manage their own affairs.</li>
		<li>In 1947, the British gave up attempting  and handed the problem over the the newly formed United Nations.</li>
		<li>MAP 1.  The UN proposed partition into Arab and Jewish sectors.  Jews welcomed the proposal, Arabs were outraged.  Note that:
			<ol>
				<li>The Arab state was roughly half of Palestine</li>
				<li>Jerusalem is under INTERNATIONAL CONTROL</li>
				<li>&quot;4 corners&quot; allow free movement between sections</li>
			</ol>
		</li>
	</ul>
</div>
  
