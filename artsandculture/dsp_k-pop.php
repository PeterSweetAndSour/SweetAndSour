<h2>Why K-pop is different&mdash;and awesome</h2>
<div class="story">
	<p>There are a handful of big entertainment companies and a bunch of smaller ones that hold auditions for those who think they can be a star and in practice, this means 13, 14, 15-year olds. (Here is the information page for <a href="https://audition.jype.com/en" target="_blank" class="external">JYP Entertainment's auditions</a> for example. You can read in the original Korean if you wish though it is not strictly necessary to speak Korean.) Alternatively, one of the company scouts might <a href="http://english.chosun.com/site/data/html_dir/2018/02/19/2018021901784.html" target="_blank" class="external">spot you while shopping</a>, think you have the look and personality they want and ask you to audition.</p>

	<p>As I understand it, if a child is selected then they leave regular school and move into the company dormitory and will then spend the next few <em>years</em> training more or less full-time in singing and dance. There are constant evaluations and some will drop out. Trainees must behave appropriately in public, are told what to eat, have very limited contact with their families, or anyone else in the outside world and are not allowed to date &ndash; the myth created is that K-pop stars are desirable but yet somehow &ldquo;available&rdquo;. Indeed, the agreements have been called <a href="https://www.pri.org/stories/2016-04-25/k-pop-s-gross-double-standard-women" target="_blank" class="external">slave contracts</a> and girls may be required to get cosmetic surgery!</p>

	<p>Then in their late teens they will be assigned, or compete for, a place in a group &ndash; all girls or all boys &ndash; and they will debut. By then, after years of practice they should be amazing&hellip; but they are the only visible part. The company employs/contracts song writers, musicians, choreographers, hair stylists, mark-up artists, fashion designers and video/sound producers and technicians to make a complete &ldquo;product&ldquo;.</p>
	
	<p>It is worth mentioning that the unblemished image created for K-pop idols means that the lyrics and the music vidoes are free from overt sexual overtones and as best I can tell from subtitles, no one swears. Performers and anyone else in the videos are fully clothed (though I will concede that skirts/dresses can be very short but they wear shorts for safety; it seems that even women showing belly buttons is a bit taboo in Korea). So, good clean fun.</p>

	<p>Hopefully the song is a hit and the trainee becomes a star (fans will adore them but they are merely company employees and the company takes the money) but that can bring its own set of pressures, sometimes with tragic consequences as the <a href="https://www.bbc.com/news/entertainment-arts-42393242" target="_blank" class="external">suicide of SHINee singer Jonghyun</a> demonstrates. More likely your catchy, beautifully executed song will get the interest of a small group of fans that will love it but most will never hear it and after a few years, the group will be disbanded. I make a point to listen to some of the lesser-known groups as some of their songs are as good as any and it is sad that they get so little recognition.</p>

	<p>Interestingly, K-pop has a known starting point in the way that <em>Rapper's Delight</em> is for hip hop. In April 1992, 3-member <strong>Seo Taiji &amp; Boys</strong> appeared on a talent show on MBC, one of Korea's TV networks performing a highly choreographed song that featured rapping. The judges were baffled and they came last. However the public loved it and the song was #1 in Korea for 17 weeks! The song: <a href="https://www.youtube.com/watch?v=JKK0UEu4Q5g" target="_blank" class="external">I know</a>.</p>
</div>

