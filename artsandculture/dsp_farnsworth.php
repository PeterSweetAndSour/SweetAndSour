<h2>Body</h2>
<h3>The boy with an idea</h3>
<div class="story">
   <ul class="endText">
   	<li><b>Philo T. Farnsworth</b> was an unlikely person to invent television</li>
   	<li>He was not a university professor or an engineer at a large company.  Men of this type were working on television but with complex mechanical devices which would never work.</li>
   	<li>Instead, Philo was a 14-year old in rural Utah but he was fascinated by science in general, and electricity in particular.. While ploughing a field in 1921, Philo looked back at the rows he had cut in the soil and realized that tiny electons could be made to draw a picture the same way, one line at at time.</li>
   </ul>
</div>

<h3>First real steps</h3>
<div class="story">
   <ul class="endText">
   	<li>At age 19, he got a chance to prove himself and his ideas.</li>
   	<li>Through a chance meeting, Philo came in contact with two investors and he was able to convince them to invest $25,000.</li>
   	<li>A small team was assembled and they threw themselves into the work.</li>
   	<li>Philo applied for patents on his main ideas in January 1927, while he was still 21.  However, the patent could not be granted until a working device was shown.</li>
   	<li>At a demonstration for one of the investors later that year, Philo, held up a black square with a white line on it and turned it in front of the camera.  The line moved on the screen.  Time magazine notes that Philo recorded in his diary, <i>"The received line picture was evident this time"</i> but the investor telegraphed the other partner saying, <i>"The damned thing works!"</i>.</li>
   	<li>The following year, Philo put on a demonstration for a group of bankers to raise more capital.  He first showed them something any banker could understand: a picture of a dollar sign ($).</li>
   </ul>
</div>

<h3>Technical triumph, business troubles</h3>
<div class="story">
   <ul class="endText">
   	<li>While the demonstration for the bankers was a great success, it signalled a new phase.</li>
   	<li>Some of the investors were hoping to put up early capital then cash out for a quick profit.  To increase the value of their holding, they contacted the press to publicise the invention.  The word was out.</li>
   	<li>Enter RCA, the Radio Corporation of America, the company that made this television.</li>
   	<li>RCA owned all the patents for radio, so anyone wanting to build radios in the 20s and 30s had to pay royalties to RCA.</li>
   	<li>However, David Sarnoff, the head of RCA, knew there was a problem.  The radio patents would reach their 17-year limit in the late 30s.  He wanted something else to take over and that something he knew would be television-radio with pictures.  RCA had to invent television, or if that wasn't possible, to buy out the person who did.</li>
   	<li>Sarnoff hired of Vladimir Zworykin, who had a working receiver, but a very poor television camera.  Obviously both are needed for a working system.  Crucially, Zworykin had filed for a patent for a camera in 1923 but the patent was pending while he attempted to build something that actually worked.</li>
   	<li>After learning of Farnsworth, Sarnoff offered $100,000 for everything&emsp;his services and the patents.</li>
   	<li>But Farnsworth said no.  Knowing television would be huge, he wanted to collect royalties, not take a one-time payment and be an employee.</li>
   	<li>Since RCA had not invented and could not buy, Sarnoff decided to set the lawyers loose on Farnsworth and his tiny company.  They charged that Farnsworth's system infringed on Zworykin's 1923 patent.</li>
   	<li> In 1934, the court ruled in Farnsworth's favor but left open the option of appeal.</li>
   	<li>In 1935, Farnworth, now in Philadelphia, staged the first public demonstration of television, an event scheduled to last 10 days but extended to 3 weeks because thousands came night and day for the 15 minute shows.</li>
   </ul>
</div>

<h3>The court appeal &ndash; the beginning of the end</h3>
<div class="story">
   <ul class="endText">
   	<li> RCA was not finished in court though.  RCA did appeal and this was the beginning of the end.</li>
   	<li>Farnsworth now desperately needed cash but no-one would invest while the patent dispute was unresolved.</li>
   	<li>To make matters worse, the FCC, the Federal Communications Commission was attemping the essential task of setting standards so all televisions were compatible with each other.  Without standards, it would be necessary to own one television for every channel!</li>
   	<li>At the 1939 World Fair in New York, RCA brought FDR along to their demonstration and promoted the idea that they invented television.  Since history is generally written by the victors, it is this event that is widely reported as the birth of television.  Farnworth's demonstrations 5 years earlier were forgotten.</li>
   </ul>
</div>

<h3>The end</h3>
<div class="story">
   <ul class="endText">
   	<li>The crippling blow came from an unexpected source.</li>
   	<li>Initially things looked hopeful.  In 1939, after 7 years of crippling litigation, the courts finally turned down RCA's appeal.  RCA would have to pay royalties to Farnsworth.</li>
   	<li>With the patent issue resolved, Farnsworth raised $300,000 in a stock float and signed a contract to purchase a factory.  The following day, Hitler invaded Czechoslovakia.</li>
   	<li>Production of television sets was stopped during the war.  Meanwhile, the time on Farnsworth's main patents was running out.</li>
   	<li>In 1946 and 1947, there was an effective freeze on sales while the FCC considered channel allocation and whether to introduce color television.</li>
   	<li>Farnsworth's company now had no patent protection and was soon bankrupt.</li>
   	<li> Meanwhile, RCA was financially strong and made big sales in the post-war years.  In 1948, there were 1 million sets in America.  In the next 4 years, 16 million sets were sold but none were made by Farnsworth.</li>
   </ul>
</div>

<h2>Conclusion</h2>
<div class="story">
   <p>So what are we to make of all this?  We should remember that Philo T. Farnsworth invented television but the credit was taken away from him.  His legacy though is undenyable.  For better or worse, television has changed the way we view and interact with the rest of the world.</p>
   <p>Philo himself was initially disappointed with the effect his invention had.  His son says that when he was growing up, his father would say "There's nothing on it worthwhile, and we are not going to watch it in this household"!  Yet in 1969, two years before he died, the world witnessed mans' first steps on the moon, a feat made possible by television.  During that event, he turned to his wife and said <i>"This has made it all worthwhile"</i>.</p>
   <p class="endText">Thank you.</p>
</div>

